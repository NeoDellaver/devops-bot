def escape_markdown_v1(text: str) -> str:
    """
    –≠–∫—Ä–∞–Ω–∏—Ä—É–µ—Ç —Ç–æ–ª—å–∫–æ —Ç–µ —Å–∏–º–≤–æ–ª—ã, –∫–æ—Ç–æ—Ä—ã–µ –ª–æ–º–∞—é—Ç —Å—Ç–∞—Ä—ã–π Markdown (v1):
    - –ù–µ—Å–±–∞–ª–∞–Ω—Å–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ * –∏ _
    - –ù–æ ! . ( ) –∏ —Ç.–¥. ‚Äî –ù–ï —ç–∫—Ä–∞–Ω–∏—Ä—É–µ–º!
    """
    # –£–±–∏—Ä–∞–µ–º –∏–ª–∏ —ç–∫—Ä–∞–Ω–∏—Ä—É–µ–º —Ç–æ–ª—å–∫–æ –ø–æ—Ç–µ–Ω—Ü–∏–∞–ª—å–Ω–æ –æ–ø–∞—Å–Ω—ã–µ –∑–≤—ë–∑–¥–æ—á–∫–∏/–ø–æ–¥—á—ë—Ä–∫–∏–≤–∞–Ω–∏—è
    # –ü—Ä–æ—Å—Ç–æ–π —Å–ø–æ—Å–æ–±: —ç–∫—Ä–∞–Ω–∏—Ä—É–µ–º –í–°–ï `*` –∏ `_`, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –Ω–µ—Å–±–∞–ª–∞–Ω—Å–∏—Ä–æ–≤–∞–Ω–Ω–æ—Å—Ç–∏
    text = text.replace("*", "\\*").replace("_", "\\_")
    # –¢–∞–∫–∂–µ —ç–∫—Ä–∞–Ω–∏—Ä—É–µ–º `[` –∏ `]`, –µ—Å–ª–∏ –æ–Ω–∏ –Ω–µ –≤ –ø–∞—Ä–µ (–Ω–æ –≤ –≤–∞—à–µ–º —Ç–µ–∫—Å—Ç–µ –∏—Ö –Ω–µ—Ç)
    return text


